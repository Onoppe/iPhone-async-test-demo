#import "RootViewController.h"

@implementation RootViewController


- (void)loadView {
    
    self.title =  @"DataApi";
    
    _view = [[UITextView alloc] initWithFrame:CGRectMake(0, 0, 320, 900)];
}

- (void) viewDidAppear:(BOOL)animated {
    
    
    [self performSelector:@selector(callNetwork) withObject:nil afterDelay:0];
}

- (void) callNetwork {
 
    
    [[NSNotificationCenter defaultCenter] addObserver:self selector:@selector(dataSuccess:) name:@"dataSuccess" object:nil];
    
    data = [[DataApi alloc] init];
    [data callNetwork];
    
}

- (void) dataSuccess:(id)object {
    
    UITextView *l = (UITextView*)self.view;
    
    l.text = data.result;
}

